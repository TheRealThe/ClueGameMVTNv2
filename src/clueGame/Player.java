package clueGame;

import java.awt.Color;

public class Player {
	private String Name;
	private int row;
	private int column;
	private Color color;
	
	public Player(String name, int row, int column, Color color) {
		super();
		Name = name;
		this.row = row;
		this.column = column;
		this.color = color;
	}
	
	

}
